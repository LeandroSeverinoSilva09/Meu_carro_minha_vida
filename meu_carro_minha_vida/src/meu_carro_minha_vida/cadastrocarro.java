package meu_carro_minha_vida;

public class cadastrocarro {

    private int idcarrosadicionados;
    private String Marca;
    private String Modelo;
    private int KmnoPainel;
    private float TabelaFipe;
    private int Anodoveiculo;
    private String Placa;

    public int getIdcarrosadicionados() {
        return idcarrosadicionados;
    }

    public void setIdcarrosadicionados(int idcarrosadicionados) {
        this.idcarrosadicionados = idcarrosadicionados;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String marca) {
        Marca = marca;
    }

    public String getModelo() {
        return Modelo;
    }

    public void setModelo(String modelo) {
        Modelo = modelo;
    }

    public int getKmnoPainel() {
        return KmnoPainel;
    }

    public void setKmnoPainel(int kmnoPainel) {
        KmnoPainel = kmnoPainel;
    }

    public float getTabelaFipe() {
        return TabelaFipe;
    }

    public void setTabelaFipe(float tabelaFipe) {
        TabelaFipe = tabelaFipe;
    }

    public int getAnodoveiculo() {
        return Anodoveiculo;
    }

    public void setAnodoveiculo(int anodoveiculo) {
        Anodoveiculo = anodoveiculo;
    }

    public String getPlaca() {
        return Placa;
    }

    public void setPlaca(String placa) {
        Placa = placa;
    }
}
